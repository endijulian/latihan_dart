void main() {
  var nilai = 'E';

  switch (nilai) {
    case 'A':
      print('Sangat Baik');
      break;
    case 'B':
      print('Baik');
      break;
    case 'C':
      print('Cukup');
      break;
    case 'E':
      print('Sangat Kurang');
      break;
    default:
      print('Nilai Kosong');
      break;
  }
}
